// Helper to assign random colors to text
export const randomizeOptions = (texts: string[]): WheelOption[] => {
    // Shuffle texts first (just to be safe, though input usually randomized)
    const shuffledTexts = [...texts].sort(() => 0.5 - Math.random());

    // Select top 8 (or less if not enough)
    const selected = shuffledTexts.slice(0, 8);

    return selected.map((label, index) => ({
        id: index + 1,
        label,
        color: COLORS[index % COLORS.length],
        textColor: '#ffffff',
    }));
};

